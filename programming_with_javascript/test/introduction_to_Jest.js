// Introduction to Jest
// Jest is a testing framework for JavaScript. It's often used for testing code like 
// React. 

// code coverage is a measure of what percetnage of my code is covered by tests.
// If I say that I have an 80% code coverage, that means that only one-fifth of 
// my entire code base is not covered by tests. But even 100% code coverage doesn't 
// mean that you have tested for every conceivable expectation. it just means that 
// there are some expectations tested for each line of my code. 

// mocking allows you to separate the code that you are testing from it's related 
// dependencies. In other words, you can use the mocking feature to make sure that your 
// unit testing is stand-alone. For example, you can test the front end functionality
// of your web app by mocking the data as if it came back from a server when in fact 
// it came from the client. Mocking is especially helpful because very often web 
// applications are built by teams of developers. Some developers work on the backend 
// feature and others work on the front end. This could result in bottlenecks. 
